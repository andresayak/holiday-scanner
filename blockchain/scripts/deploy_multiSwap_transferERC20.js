async function main() {

    const [account] = await ethers.getSigners();

    const balance = await account.getBalance();
    console.log(' - account address: ' + account.address);
    console.log(' - account balance: ' + balanceHuman(balance));

    const token = await ethers.getContractAt(abi, TOKEN_ADDRESS, account);

    console.log(await token.name());

    const amountIn = ethers.utils.parseEther("1000");

    console.log('amountIn=' + amountIn);

    const multiSwapAddress = '0x12555A6D24486DDad45ac0a7f836dEB17f195f72';

    const address = "0xf977814e90da44bfa03b6295a0616a897441acec";
    await helpers.impersonateAccount(address);
    const impersonatedSigner = await ethers.getSigner(address);

    const tx2 = await token.connect(impersonatedSigner).transfer(multiSwapAddress, amountIn.toString());
    console.log('transfer tx');
    await tx2.wait();
    console.log('transfer receipt');

    console.log('multiSwap balance='+await token.balanceOf(multiSwapAddress));

}

main()
    .then(() => process.exit(0))
